
package boletin9_4word;


public class multi {
        public void multi(int num){
            int i=1; //damos el valor 1 a i
             
        do{//abrimos do para que nos calcule numero que le demos * 1
            System.out.println(num + " * " + i + " = " + num*i);// hacemos el calculo numero que queremos por 1 en este caso
            //ya que a i le dimos el valor 1 por defecto.
            
        }while (i<=10);//i tiene que ser menor o igual que 10 si no da error 
    }

    
}
    

